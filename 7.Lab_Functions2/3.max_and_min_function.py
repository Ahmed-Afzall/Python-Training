# 3. Using max() and min() functions display the maximum and minimum of 5 random numbers. 

# I have manually provided 5 random numbers in a list
numbers = [10, 25, 36, 47, 5]

# Finding the maximum and minimum using max() and min() function
maximum = max(numbers)
minimum = min(numbers)

#printing the max and min numbers from the list
print("Numbers:", numbers)
print("Maximum:", maximum)
print("Minimum:", minimum)