# Q.2 Write a program for assignment operators

a = 10
b = 20 #'b' is only used in modulus because if we continued with 'a' the output would have been '0'.

# Assign
a = 10
print("Assign:", a)

# Add and assign
a += 5
print("Add and assign:", a)

# Subtract and assign
a -= 3
print("Subtract and assign:", a)

# Multiply and assign
a *= 2
print("Multiply and assign:", a)

# Divide and assign
a /= 4
print("Divide and assign:", a)

# Modulus and assign
b %= 3
print("Modulus and assign:", b)

# Exponentiation and assign
a **= 2
print("Exponentiation and assign:", a)

# Floor division and assign
a //= 2
print("Floor division and assign:", a)
