# Q.3 Write a program for bitwise operators

a = 10  # Binary: 1010
b = 4   # Binary: 0100

# Bitwise AND
bitwise_and = a & b
print("Bitwise AND:", bitwise_and)

# Bitwise OR
bitwise_or = a | b
print("Bitwise OR:", bitwise_or)

# Bitwise XOR
bitwise_xor = a ^ b
print("Bitwise XOR:", bitwise_xor)

# Bitwise NOT
bitwise_not = ~a
print("Bitwise NOT:", bitwise_not)

# Bitwise left shift
left_shift = a << 2
print("Left Shift:", left_shift)

# Bitwise right shift
right_shift = a >> 2
print("Right Shift:", right_shift)
