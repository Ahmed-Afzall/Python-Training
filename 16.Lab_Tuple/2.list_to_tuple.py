'''2.Write a Python program to convert a list to a tuple. 
Input: listx = [5, 10, 7, 4, 15, 3] '''

# Defining the list
listx = [5, 10, 7, 4, 15, 3]

# Converting the list into a tuple using the tuple() function
tuplex = tuple(listx)

# Printing the result
print("Converted tuple:", tuplex)